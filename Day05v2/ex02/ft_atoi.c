int	ft_atoi(char *str)
{
	int nb;
	int isneg;

	nb = 0;
	isneg = 1;
	while ((*str >= 9 && *str <= 13) || (*str == ' '))
	{
		str++;
	}
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			isneg = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		nb = nb * 10 + *str++ - '0';
	}
	return (nb * isneg);
}
