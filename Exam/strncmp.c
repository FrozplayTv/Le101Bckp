int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int i;

	i = 0;
	while ((*s1 == *s2) && (i < n))
	{
		if (*s1 == '\0')
			return (0);
		s1++;
		s2++;
		i++;
	}
	if (*s1 != *s2)
		return (*s1 - *s2);
	return (0);
}
